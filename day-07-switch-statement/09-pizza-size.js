const size = "L";

switch (size) {
    case "S":
        console.log("Small Pizza");
        break;
    case "M":
        console.log("Medium Pizza");
        break;
    case "L":
        console.log("Large Pizza");
        break;
    case "XL":
        console.log("Extra Large Pizza");
        break;
    default:
        console.log("Invalid Size");
}