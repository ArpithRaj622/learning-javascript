const age = 24;
const marks = "24";
const passwordLength = 8;
const height = 177;

// equal to
console.log(`${age} == "${marks}" -> ${age == marks}
`);

// strict equal to
console.log(`${age} === "${marks}" -> ${age === marks}
`);

// not equal to
console.log(`${age} != "${marks}" -> ${age != marks}
`);

// strict not equal to
console.log(`${age} !== "${marks}" -> ${age !== marks}
`);

// greater than
console.log(`passwordLength(${passwordLength}) > 8 -> ${passwordLength>8}
`);

// less than
console.log(`passwordLength(${passwordLength}) < 8 -> ${passwordLength<8}
`);

// greater than or equal to
console.log(`passwordLength(${passwordLength}) >= 8 -> ${passwordLength>=8}
`);

// less than or equal to
console.log(`height(177) <= 180 -> ${height<=180}`);