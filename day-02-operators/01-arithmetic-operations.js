const num1 = 66;
const num2 = 99;

// addition
console.log(`Addition:
${num1} + ${num2} = ${num1+num2}
`);

// subtraction
console.log(`Subtraction:
${num2} - ${num1} = ${num2-num1}
`);

// multiplication
console.log(`Multiplication:
${num1} * ${num2} = ${num1*num2}
`);

// division
console.log(`Division:
${num2} / ${num1} = ${num2/num1}
`);

// modulus
console.log(`Modulus (remainder):
${num2} % ${num1} = ${num2%num1}
`);

// exponentiation
console.log(`Exponentiation:
${num1} ** 2 = ${num1**2}
${num2} ** 3 = ${num2**3}
${num2} ** ${num1} = ${num2**num1}
`);