let num = 999;

num = 666;
console.log(`num = ${num}
`);

// num += 2 -> num = num + 2
console.log(`${num} += 2 -> ${num+=2}
`);

// num -= 2 -> num = num - 2
console.log(`${num} -= 2 -> ${num-=2}
`);

// num *= 2 -> num = num * 2
console.log(`${num} *= 2 -> ${num*=2}
`);

// num /= 2 -> num = num / 2
console.log(`${num} /= 2 -> ${num/=2}
`);

// num %= 4 -> num = num % 4
console.log(`${num} %= 4 -> ${num%=4}
`);

// num **= 2 -> num = num ** 2
console.log(`${num} **= 2 -> ${num**=2}
`);