let num = 666;

// pre-increment
console.log(`Pre-increment:
Before increment num = ${num}
After increment ++num = ${++num}
`);

// post-increment
console.log(`Post-increment:
Before increment num = ${num}
After increment num++ = ${num++}
After the above line execution, num = ${num}
`);

// pre-decrement
console.log(`Pre-decrement:
Before decrement num = ${num}
After decrement --num = ${--num}
`);

// post-decrement
console.log(`Post-decrement:
Before decrement num = ${num}
After decrement num-- = ${num--}
After the above line execution, num = ${num} 
`);